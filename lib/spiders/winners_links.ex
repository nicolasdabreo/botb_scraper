defmodule WinnersLinks do
  @moduledoc """
  This spider extracts all the `winners` absolute urls from the botb website.

  Because botb employs a specific javascript based pagination method a browser
  renderer cannot access all the links the spider needs. Instead scroll the page
  yourself and copy the source into a html file on a running webserver and
  extract all the links from that instead.

  Prints the list of links for you to add to the module attribute here as well
  as the total number of links scraped.
  """
  use Crawly.Spider

  require Logger

  @impl Crawly.Spider
  def base_url(), do: "https://www.botb.com"

  @impl Crawly.Spider
  def init() do
    [start_urls: ["localhost:4000/foo"]]
  end

  @impl Crawly.Spider
  def parse_item(response) do
    # Parse response body to document
    {:ok, document} = Floki.parse_document(response.body)

    winner_page_urls =
      document
      |> Floki.find("article.timeline")
      |> Floki.find("a")
      |> Enum.filter(fn x ->
        url = Floki.attribute(x, "href")
        text = Floki.text(x)
        winner_page_url?(url, text)
      end)
      |> Floki.attribute("href")
      |> Crawly.Utils.build_absolute_urls(base_url())

    IO.inspect(winner_page_urls)
    IO.inspect(length(winner_page_urls))

    %{items: [], requests: []}
  end

  defp winner_page_url?([url], text) when is_binary(url) and is_binary(text) do
    url =~ "winners"
  end

  defp winner_page_url?(_url, _text), do: false

